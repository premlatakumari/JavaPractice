package PepInsta.String;

public class _4LengthOfString {
    public static void main(String[] args) {
        String str="Somee";
        int count=0;
        for(int i=0;i<str.length();i++){
            count++;
        }
        System.out.println(count);
        //System.out.println(str.length());
    }
}
