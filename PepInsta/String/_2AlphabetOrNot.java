package PepInsta.String;

public class _2AlphabetOrNot {
    public static void main(String[] args) {
        char ch='1';
        if((ch>='A' && ch<='Z')||(ch>='a' && ch<='z')){
            System.out.println("its a alphabet");
        }
        else{
            System.out.println("its not a alphabet");
        }
    }
}
